local zmq = require'handler.zmq'
local ev = require'ev'
local loop = ev.Loop.default

local ctx = zmq.init(loop, 1)

-- define SUB worker
local function handle_msg(sock, data)
  print(data)
end

-- create SUB worker
local zsub = ctx:sub(handle_msg)

zsub:sub("")
zsub:connect("tcp://localhost:5555")

loop:loop()

